	<div>
		<h2>Timo Herzog</h2>
		<p><i>- Geboren als Mann, entwickelt zum ultimativen Skispringer.</i></p>
		<p>
			Er wurde Ende des 18. Jahrhunderts in der <a
				href="https://www.google.de/maps/place/Telemark,+Norway/@59.4151535,8.5013811,7z/data=!3m1!4b1!4m6!3m5!1s0x4638abff0780bcc9:0x301772628b31c90!8m2!3d59.3913985!4d8.3211209!16zL20vMDE3bjlf?entry=ttu"
				>norwegischen Provinz Telemark</a
			> geboren, wo er mit 5 Jahren den ersten Skisprung in der Weltgeschichte absetzte. In den Anfängen
			sprang er hier noch über größere Schneehügel. Entwickelte dann aber seine Technik schnell weiter
			und sprang über schneebedeckte Holzhaufen und Schäunendächer.
		</p>
		<p>
			Nach kurzer Zeit zog er so viele Touristen nach Telemark, dass die Österreicher auf ihn
			aufmerksam wurden und ihn ein Angebot machten, dass er mit seinen jungen 12 Jahren nicht
			ablehnen konnte.
		</p>
		<p>
			Die Herrschaften aus Innsbruck setzten ihm das Ultimatum, dass er den größten und weitesten
			Sprung der Weltgeschichte vor den Augen einer riesigen Zuschauer Masse hinlegen musste, um ein
			Preisgeld von <b>1ner Septillionen Schilling</b> zu erhalten. Innerhalb der nächsten 6 Jahre Trainierte
			er im außen Gelände im tiefsten Schnee von Innsbruck mit Wölfen und Rehen, um den stärksten, aber
			auch agilsten Sprung zu erlernen, den je ein Mensch auf der Welt gesehen hatte.
		</p>
		<p>
			Am 02. Februar 1891 war es so weit und Timo Herzog wagte sich an den wohl krassesten Sprung in
			seiner Karriere. Den ersten Sprung landete er nur knapp und die Menge jubelte. Als er das
			Jubeln der Leute hörte gab er eine Zugabe, die die Leute vor Aufregung fast ins Grab brachte.
		</p>
		<br />
		<h3>> "Da kommt er", schrie ein Mann in der Menge.</h3>
		<br />
		<h3>
			> "Zeig uns den krassesten Trick!", schrie eine Frau aus der Meute und entblößte ihre beiden
			Germknödel.
		</h3>
		<br />
		<p>
			Vor Spannung war die Luft schon so stark geladen, dass <span class="blitze">Blitze</span> den Himmel
			durchzogen!!!
		</p>
		<p>
			Timo trat an die Skischanze, holte noch ein letztes Mal Luft und stoß! Mit knapp 150 Sachen
			bretterte er auf seinen Holzskiern die Schanze hinunter und zeigte der Welt den wohl
			krassesten Trick der Erde.
		</p>
		<p>
			Der nach ihm benannte <b
				>"FAKIE, NOLLIE, SWITCH, LATE, BIG, Bigger FLIP mit MEHREREN FLIPS dazwischen"</b
			> <i>krass… Dafür finde sogar ich als Erzähler hier keine Worte.</i> Nachdem Timo den Trick landete,
			ließ er sich kurz bejubeln, holte die Septillionen Schillinge von der Bank ab und lebt bis heute
			Glücklich in seiner Badewanne voller Geld. Nebenbei brachte er sich selbst das Stricken bei.
		</p>
		<h3>FIN</h3>
